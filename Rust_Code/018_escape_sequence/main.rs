fn main() {
    // \n for new line
    println!("Hello\nWorld!");

    // "\" for double quote
    println!("Hello\"World!");

    // \' for single quote
    println!("Hello\'World!");

    // \\ for backslash
    println!("Hello\\World!");

    // \t for tab
    println!("Hello\tWorld!");

    // \r for carriage return
    println!("abcd efgh \r123456 pro");
}
