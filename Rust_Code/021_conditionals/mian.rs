fn main() {
    // if else
    let age = 10;

    if age >= 18 {
        println!("You can vote");
    } else {
        println!("You can't vote");
    }

    //  else if
    let number = 0;
    if number > 0 {
        println!("Positive number");
    } else if number < 0 {
        println!("Negative number");
    } else {
        println!("Zero");
    }

    // if

    let num = 12;
    if num == 12 {
        println!("This is equal to 12");
    }

    println!("print after if");
}
